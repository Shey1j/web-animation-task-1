      const totalImages = this.toBePreloaded.length
      let loadedCount = 0

      const min3Digits = (num) => {
        if (num < 10) {
          return `00${num}`
        } else if (num < 99) {
          return `0${num}`
        }
        return `${num}`
      }
      this.toBePreloaded.forEach((image, index) => {
